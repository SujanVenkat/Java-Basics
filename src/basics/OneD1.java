package basics;
import java.util.*;
public class OneD1{
	public static void main(String args[]) {
		Scanner sc=new Scanner(System.in);
		int d=sc.nextInt();
		int [] f=new int[d];
		for(int i=0;i<f.length;i++) {
		f[i]=sc.nextInt();
	}
		for(int i=0;i<f.length;i++) {
		System.out.print(f[i]+" ");
		
		
	}
}
}